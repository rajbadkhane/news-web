export const manualNewsRecords: RemoteNewsRecord[] = [LAWRENCE_GANG_ARTICLE, GUNJA_ARTICLE];

export function getManualNewsRecordById(idOrSlug: string) {
  return manualNewsRecords.find(
    (record) => record.id === idOrSlug || record.publication.slug === idOrSlug || record.news.id === idOrSlug
  );
}

export function mergeManualNewsIntoGroups(groups: RemoteNewsGroup[]) {
  const nextGroups = [...groups];

  manualNewsRecords.forEach((record) => {
    const existingGroup = nextGroups.find((group) => group.category === record.news.category);

    if (existingGroup) {
      const existingRecords = existingGroup.records.filter((item) => item.id !== record.id);
      existingGroup.records = [record, ...existingRecords];
      existingGroup.count = existingGroup.records.length;
      return;
    }

    nextGroups.unshift({
      category: record.news.category,
      count: 1,
      records: [record]
    });
  });

  return nextGroups;
}

export function mergeManualNewsIntoCategory(category: string, group?: RemoteNewsGroup) {
  const manualRecords = manualNewsRecords.filter((record) => record.news.category === category);

  if (!manualRecords.length) {
    return group;
  }

  if (!group) {
    return {
      category,
      count: manualRecords.length,
      records: manualRecords
    };
  }

  const existingIds = new Set(manualRecords.map((record) => record.id));
  const mergedRecords = [...manualRecords, ...group.records.filter((record) => !existingIds.has(record.id))];

  return {
    ...group,
    count: mergedRecords.length,
    records: mergedRecords
  };
}
